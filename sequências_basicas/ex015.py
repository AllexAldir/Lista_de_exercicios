# 15) Crie um programa que leia o número de dias trabalhados em um mês e mostre o
#     salário de um funcionário, sabendo que ele trabalha 8 horas por dia e ganha R$25
#     por hora trabalhada.

dias_tra = int(input("Digite o número de dias trabalhados durante 1 mês do funcionário: "))
print(f"Salário de acordo com os dias trabalhados desse funcionário: {dias_tra * 200}")